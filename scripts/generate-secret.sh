    ESCAPED_SECRET=$(echo "$SECRET" | sed 's/[[\.*^$()+?{|]/\\&/g')
    sed -i '' "s/METRICS_SECRET=.*/METRICS_SECRET=$ESCAPED_SECRET/" .env
    echo "âœ… Updated existing METRICS_SECRET"
else
